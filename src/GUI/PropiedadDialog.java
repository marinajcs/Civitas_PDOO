    void rellenaPropiedades(Jugador jugador){
        DefaultListModel propiedades = new DefaultListModel<>(); // datos para la lista
        
        for (CasillaCalle t: jugador.getPropiedades()){
            propiedades.addElement(t.getNombre());
        } //se completan los datos
        listaPropiedades.setModel(propiedades); //se le dice a la lista cu√°les son esos datos
    }

